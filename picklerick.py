# - –ì–∏–±–∫–æ—Å—Ç—å: –ü–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ—Ç –ø—Ä–∞–∫—Ç–∏—á–µ—Å–∫–∏ –≤—Å–µ —Ç–∏–ø—ã –¥–∞–Ω–Ω—ã—Ö Python.

# - –ë—ã—Å—Ç—Ä–æ–¥–µ–π—Å—Ç–≤–∏–µ: Pickle —Ä–∞–±–æ—Ç–∞–µ—Ç –±—ã—Å—Ç—Ä–µ–µ, —á–µ–º –¥—Ä—É–≥–∏–µ —Ñ–æ—Ä–º–∞—Ç—ã, —Ç–∞–∫–∏–µ –∫–∞–∫ JSON.

